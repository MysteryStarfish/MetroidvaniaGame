using System.Collections;
using UnityEngine;

public class NormalAttack : AttackSet
{
    public override void Initialize()
    {
        base.Initialize();
    }
    protected override void NextAttack()
    {
        base.NextAttack();
    }
    public override void NextAttackNum()
    {
        base.NextAttackNum();
    }
    public override IEnumerator Run()
    {
        return base.Run();
    }
}
